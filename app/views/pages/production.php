        </li>
        <li>
          <strong>Транзисторы</strong> <br>
          - Биполярные, полевые, MOSFET, IGBT и другие типы транзисторов. <br>
          - Разработка схем и компонентов для управления мощными нагрузками, усиления сигналов и т. д. <br>
          - Подбор оптимальных технологических решений в зависимости от задач (силовая электроника, высокочастотные модули, цифровые схемы).
        </li>
        <li>
          <strong>Конденсаторы</strong> <br>
          - Электролитические, керамические, танталовые и плёночные типы. <br>
          - Возможность поставки широкого диапазона ёмкостей и напряжений, оптимальных по стоимости и срокам. <br>
          - Индивидуальный подбор и производство под специфические температурные режимы и условия эксплуатации (например, для автомобильной, военной или промышленной электроники).
        </li>
        <li>
          <strong>Диоды</strong> <br>
          - Шоттки, выпрямительные, стабилитроны, варикапы и пр. <br>
          - Под различные диапазоны токов и напряжений, в том числе высоковольтные и высокоскоростные диоды. <br>
          - Возможность кастомизации корпуса, параметров и маркировки под требования заказчика.
