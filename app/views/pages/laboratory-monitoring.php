    <h2>Что даёт лабораторный контроль качества</h2>
    <div style="text-align: justify;">
      <ol>
        <li>
          <strong>Выявление скрытых дефектов</strong> <br>
          На ранних этапах проверок можно обнаружить микротрещины, дефекты пайки, проблемы с герметизацией или отклонения в электрических параметрах, которые на первый взгляд могут быть незаметны.
        </li>
        <li>
          <strong>Соответствие отраслевым стандартам</strong> <br>
          Регулярное тестирование обеспечивает надёжность продукции и помогает соблюдать жёсткие стандарты (например, MIL-STD, IPC, ГОСТ и др.), необходимые для сертификации и выхода на международный рынок.
        </li>
        <li>
          <strong>Оптимизация производственных процессов</strong> <br>
          Результаты лабораторных испытаний позволяют производителям скорректировать используемые материалы, технологии пайки и сборки, а также условия хранения и транспортировки компонентов.
        </li>
        <li>
          <strong>Предотвращение финансовых потерь</strong> <br>
          Своевременная проверка микросхем, транзисторов, конденсаторов, диодов и других электронных компонентов снижает риск массового брака и связанных с ним затрат — от отзывов продукции до репутационных издержек.
        </li>
      </ol>
      <section class="prose__section">
        <h4><strong>Услуги нашей лаборатории</strong></h4>
        <p>Наша компания предлагает комплексное решение по <strong>лабораторному контролю качества</strong>, включая:</p>
        <ol>
          <li>
            <strong>Визуальный и рентген-контроль</strong>
            <ul>
              <li>Микроскопический осмотр для оценки качества пайки, монтажа и целостности конструкции.</li>
              <li>Рентгеновский анализ (X-ray) для выявления внутренних дефектов, таких как пустоты в шариках BGA, трещины в корпусе или отслоения подшаровых соединений.</li>
            </ul>
          </li>
          <li>
            <strong>Электрические тесты</strong>
            <ul>
              <li>Проверка рабочих характеристик и соответствия спецификациям (ток, напряжение, частота, время отклика и т.д.).</li>
              <li>Диагностика функции и параметров выхода (pin-to-pin testing).</li>
              <li>Тестирование под нагрузкой и в разных температурных режимах, чтобы убедиться в надёжности при реальной эксплуатации.</li>
            </ul>
          </li>
          <li>
            <strong>Анализ материального состава</strong>
            <ul>
              <li>Спектральное исследование и химический анализ для определения соответствия используемых материалов отраслевым и экологическим нормам (RoHS, REACH).</li>
              <li>Контроль качества металлизации и напыления в корпусах и соединениях.</li>
            </ul>
          </li>
          <li>
            <strong>Климатические и механические испытания</strong>
            <ul>
              <li>Тесты при экстремальных температурах (термошоки, повышенная влажность) и проверка на вибрации или механические удары.</li>
              <li>Имитация жизненного цикла микросхемы и оценка её поведения на протяжении всего срока службы.</li>
            </ul>
          </li>
          <li>
            <strong>Разрушающий анализ (Destructive Physical Analysis, DPA)</strong>
            <ul>
              <li>Полная разборка компонента для проверки соответствия внутренней структуры и качества сборки заявленным требованиям.</li>
              <li>Выявление источников возможных отказов: отслаивание кристалла, качество соединений, поломка выводов.</li>
            </ul>
          </li>
          <li>
            <strong>Отчётность и консультации</strong>
            <ul>
              <li>Готовим подробные отчёты со всеми результатами экспертиз, фотографиями и графиками.</li>
              <li>При необходимости предлагаем рекомендации по улучшению процесса производства, подбору компонентов, условиям хранения и эксплуатации.</li>
            </ul>
          </li>
        </ol>
      </section>
